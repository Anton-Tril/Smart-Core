        private static eBuiltInIcons _window;

        public static void Execute(Action<string> onSelect)
        {
            if (_window) { _window.Close(); return; }

            _window = GetWindow<eBuiltInIcons>(false, "Built-in Icons");
            _window._onSelect = onSelect;
            _window.minSize = new Vector2(300, 300);
            _window.titleContent.image = eIcons.Get("icons/d_sceneviewfx.png");
            _window.titleContent.text = "Built-in Icons";
            _window.Show();
            _window.Focus();
        }

        private Action<string> _onSelect;
        private Vector2 _scrollPosition;
        private static readonly List<string> _pathList = new List<string>();
        private string _searchText;
        private int _searchSizeMin;
        private int _searchSizeMax;

        void OnGUI()
        {
            if (_pathList.Count == 0) FillIconLists();
            eGUI.RememberColors();

            GUILayout.BeginHorizontal();
            {
                eGUI.TextEditor(ref _searchText, "Search");
                GUILayout.Label("Min", GUILayout.ExpandWidth(false));
                eGUI.IntEditor(ref _searchSizeMin, null, 35);
                GUILayout.Label("Max", GUILayout.ExpandWidth(false));
                eGUI.IntEditor(ref _searchSizeMax, null, 35);
                if (eGUI.ButtonMini(eIcons.Get("d_winbtn_win_close"), "Clear", eGUI.redLt, 18))
                {
                    _searchText = "";
                    _searchSizeMin = 0;
                    _searchSizeMax = 0;
                }
            }
            GUILayout.EndHorizontal();

            var search = string.IsNullOrEmpty(_searchText) ? null : _searchText.ToLower();
            _scrollPosition = GUILayout.BeginScrollView(_scrollPosition, false, false);
            {
                foreach (var path in _pathList)
                {
                    if (string.IsNullOrEmpty(path)) continue;
                    if ((search != null) && !path.Contains(search)) continue;

                    var tex = eIcons.Get(path);
                    if (tex == null) continue;
                    if (_searchSizeMax > 0 && (tex.width > _searchSizeMax || tex.height > _searchSizeMax)) continue;
                    if (_searchSizeMin > 0 && (tex.width < _searchSizeMin || tex.height < _searchSizeMin)) continue;

                    EditorGUILayout.BeginHorizontal();
                    {
                        DrawItem(tex, path);
                    }
                    EditorGUILayout.EndHorizontal();
                }
                GUILayout.EndScrollView();
            }
        }

        private void DrawItem(Texture2D tex, string path)
        {
            eGUI.ResetFG();
            if (GUILayout.Button(tex, GUILayout.Width(50), GUILayout.Height(32)))
            {
                if (_onSelect == null)
                {
                    EditorGUIUtility.systemCopyBuffer = path;
                    if (Event.current.control) // save texture to file
                    {
                        // save 16x16 mipmap if shift pressed
                        var w = (Event.current.shift) ? 16 : tex.width;
                        var h = (Event.current.shift) ? 16 : tex.height;
                        var rt = new RenderTexture(w, h, 32);
                        var nt = new Texture2D(w, h, TextureFormat.ARGB32, false);
                        Graphics.Blit(tex, rt);
                        RenderTexture.active = rt;
                        nt.ReadPixels(new Rect(0, 0, w, h), 0, 0);
                        RenderTexture.active = null;
                        File.WriteAllBytes(@"C:\UnityBuiltInIcon.png", nt.EncodeToPNG());
                    }
                }
                else
                {
                    _onSelect(path);
                    _onSelect = null;
                    Close();
                }
            }
            EditorGUILayout.BeginVertical();
            {
                eGUI.SetColor(eGUI.orangeLt);
                EditorGUILayout.LabelField(path.Replace("icons/", ""));
                eGUI.SetColor(eGUI.grayLt);
                EditorGUILayout.LabelField(tex.width + "x" + tex.height);
                eGUI.ResetColors();
            }
            EditorGUILayout.EndVertical();
        }

        private static void BuildIconLists()
        {
            byte[] buff;
            using (var fs = new FileStream(@"c:\Program Files\Unity\Editor\Data\Resources\unity editor resources", FileMode.Open))
            {
                buff = new byte[fs.Length];
                fs.Read(buff, 0, (int) fs.Length);
            }

            var i = 0;
            using (var sw = new StreamWriter(@"C:\1.txt"))
                while (i < buff.Length)
                {
                    if (buff[i++] != 'i') continue;
                    if (buff[i++] != 'c') continue;
                    if (buff[i++] != 'o') continue;
                    EditorUtility.DisplayProgressBar("Processing", "Scanning buffer", (float) i / buff.Length);
                    if (buff[i++] != 'n') continue;
                    if (buff[i++] != 's') continue;
                    if (buff[i++] != '/') continue;

                    var str = "";
                    while (i < buff.Length)
                    {
                        if (str.Length > 200)
                        {
                            Debug.Log("Over: " + str);
                            break;
                        }
                        str += (char) buff[i++];
                        if (str.EndsWith(".png") || str.EndsWith(".asset"))
                        {
                            _pathList.Add("icons/" + str);
                            sw.WriteLine("\t_pathList.Add(\"icons/" + str + "\");");
                            break;
                        }
                    }
                }

            EditorUtility.ClearProgressBar();
        }

